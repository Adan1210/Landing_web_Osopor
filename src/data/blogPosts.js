export function formatDate(iso, lang) {
  const d = new Date(iso + "T12:00:00");
  const locales = { es: "es-PE", en: "en-US", zh: "zh-CN" };
  return d.toLocaleDateString(locales[lang] || "es-PE", { year: "numeric", month: "long", day: "numeric" });
}
